package com.gublen.infopill.custom.adapter;

public class Pill {

    private String title;
    private String subitem;

    public Pill(String title, String subitem) {
        this.title = title;
        this.subitem = subitem;
    }

    public String getTitle() {
        return title;
    }

    public String getSubitem() {
        return subitem;
    }

}
